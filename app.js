// display board in browser
function createBoard() {
    startPieces.forEach((startPiece, index) => {
        const square = document.createElement('div');
        square.classList.add('square');
        square.innerHTML = startPiece;

        // If square has a piece or first child element then add draggable class to piece element
        // Optional chaining operator: If first child is null or undefined
        // then setAttribute will not be called
        square.firstChild?.setAttribute('draggable', true);

        square.setAttribute('square-id', index);

        // Add background color to squares on board
        const row = Math.floor((63 - index) / 8) + 1;
        if (row % 2 === 0) {
            square.classList.add(index % 2 === 0 ? 'beige': 'brown');
        } else {
            square.classList.add(index % 2 === 0 ? 'brown': 'beige');

        } 

        // First 16 squares on the board
        if (index <= 15) {
            // targets the svg inside piece div (square -> piece -> svg)
            square.firstChild.firstChild.classList.add('black');
        }

        if (index >= 48) {
            square.firstChild.firstChild.classList.add('white');
        }

        gameboard.append(square);

    })
}


